                        <div class="six columns">
                            <h1>
                                PhillySportPast Logo
                            </h1>

                            <ul class="stats-tabs">
                                <li><span>Client:<b>PhillySportPast</b></span></li>
                                <li><span>Title:<b>PhillySportPast Logo</b></span></li>
                                <li><span>Date:<b>July 2012</b></span></li>
                                <li><span>Type:<b>Logo Design</b></span></li>
                            </ul>
