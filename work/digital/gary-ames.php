                        <div class="six columns">
                            <h1>
                                <a class="no-underline" href="https://github.com/chasewoodford/garyames.net" title="Check out the code" target="_blank">garyames.net</a>
                            </h1>

                            <ul class="stats-tabs">
                                <li><span>Client:<b>Gary Ames</b></span></li>
                                <li><span>Title:<b>garyames.net</b></span></li>
                                <li><span>Date:<b>April 2013</b></span></li>
                                <li><span>Type:<b>Website</b></span></li>
